void process_command(char *cmd)
{
	if(strcmp(cmd,"led on")==0){
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_SET);
		led_state=1;
		HAL_UART_Transmit(&huart1,(uint8_t*)"led on\r\n",8,100);
	}
	else if(strcmp(cmd,"led off")==0){
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_RESET);
		led_state=0;
		HAL_UART_Transmit(&huart1,(uint8_t*)"led off\n\r",9,100);
	}
	else if(strcmp(cmd,"led toggle")==0){
		HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);
		HAL_UART_Transmit(&huart1,(uint8_t*)"led toggled\n\r",13,100);
	}
	else if(strcmp(cmd,"status")==0){
		if(led_state)
			HAL_UART_Transmit(&huart1,(uint8_t*)"led is on\r\n",strlen("led is on\r\n"),100);
		else
			HAL_UART_Transmit(&huart1,(uint8_t*)"led is off\r\n",strlen("led is off\r\n"),100);
	}
	else{
		HAL_UART_Transmit(&huart1,(uint8_t*)"Unknown command\r\n",18,100);
	}
}